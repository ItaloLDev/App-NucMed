package prototipo.italoluis.com.fireprot3;

public class NewAutores {

    private String nome_author;
    private String email_author;
    private String nome_padrinho;

    public NewAutores() {
    }

    public String getNome_author() {
        return nome_author;
    }

    public void setNome_author(String nome_author) {
        this.nome_author = nome_author;
    }

    public String getEmail_author() {
        return email_author;
    }

    public void setEmail_author(String email_author) {
        this.email_author = email_author;
    }

    public String getNome_padrinho() {
        return nome_padrinho;
    }

    public void setNome_padrinho(String nome_padrinho) {
        this.nome_padrinho = nome_padrinho;
    }
}
